document.addEventListener('keydown', function (event) {
  switch (event.key) {
    case '1':
      window.location.href = "index.html";
      break;
    case '2':
      window.location.href = "projects.html";
      break;
    case '3':
      window.location.href = "skills.html";
      break;
    case '4':
      window.location.href = "contact.html";
      break;
  }
});
